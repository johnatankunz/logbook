package com.prologg.logbook.dto;

public class TotalPeriodoDTO {

	private Integer codigo;

	private String descricao;

	private String tempoRecomendado;

	private String totalPeriodo;

	private String horasNoturnasClt;

	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getTempoRecomendado() {
		return tempoRecomendado;
	}

	public void setTempoRecomendado(String tempoRecomendado) {
		this.tempoRecomendado = tempoRecomendado;
	}

	public String getTotalPeriodo() {
		return totalPeriodo;
	}

	public void setTotalPeriodo(String totalPeriodo) {
		this.totalPeriodo = totalPeriodo;
	}

	public String getHorasNoturnasClt() {
		return horasNoturnasClt;
	}

	public void setHorasNoturnasClt(String horasNoturnasClt) {
		this.horasNoturnasClt = horasNoturnasClt;
	}

}
